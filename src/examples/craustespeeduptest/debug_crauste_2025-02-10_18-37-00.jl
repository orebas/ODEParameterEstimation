# Create new PEP with our specific parameters and initial conditions
p_true = OrderedDict(muN => 0.539, muEE => 0.672, muLE => 0.582, muLL => 0.536, muM => 0.439, muP => 0.617, muPE => 0.45, muPL => 0.813, deltaNE => 0.871, deltaEL => 0.407, deltaLM => 0.733, rhoE => 0.523, rhoP => 0.554)
